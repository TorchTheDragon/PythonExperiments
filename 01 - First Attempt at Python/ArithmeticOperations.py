# Arithmetic Operations
x = 10

x = x + 3 # These two are the same
x += 3 # Augmented Assignment Operator
# Works with -=, *=, and /=

print(x)

# Examples
print(10 // 3) # Gives Int
print(10 / 3) # Gives Float
print(10 % 3) # Gives remainder of the division
print(10 ** 3) # How to say 10 to the power of 3